/**
 * 根据图标名称获取图标组件
 * @param {string} iconName - 图标名称
 * @returns {Object} 图标组件
 */
export const getIconComponent = (iconName) => {
  if (!iconName) {
    return Document; // 默认图标
  }
  return ICON_MAP[iconName] || Document;
};

